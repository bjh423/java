package 배열;

public class 배열개념 {

	public static void main(String[] args) {
		// 1. 값을 이미 아는 경우
		// 2. 공간을 만들어두고 나중에 값을 넣는 경우
		int[] q = {1,2,3,4};
		System.out.println(q);
		System.out.println(q[0]);
		System.out.println(q[1]);
		System.out.println(q[2]);
		System.out.println(q[3]);
		System.out.println(q.length);
		
	}

}
